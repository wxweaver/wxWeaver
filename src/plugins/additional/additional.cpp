        wxPickerBase* picker = dynamic_cast<wxPickerBase*>(obj);
        if (!picker)
            return;

        picker->GetPickerCtrl()->Disconnect(
            wxEVT_LEFT_DOWN,
            wxMouseEventHandler(PickerComponentBase::OnLeftClick),
            nullptr, this);

